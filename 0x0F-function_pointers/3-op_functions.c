/**
 * op_add - adds two integers
 * @a: first integer
 * @b: second integer
 * Return: a+b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Finds the difference of two integers
 * @a: first integer
 * @b: second integer
 * Return: a-b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplies two integers
 * @a: First integer
 * @b: Second integer
 * Return: a*b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divides two integer
 * @a: first integer
 * @b: second integer
 * Return: a/b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Finds the modulus of two integers
 * @a: first integer
 * @b: second integer
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
